class Product {
  final int id;
  final String photoUrl;
  final String name;
  final String description;
  final double price;
  final double rating;
  final String type;
  final String detailedDescription;

  Product({
    required this.id,
    required this.photoUrl,
    required this.name,
    required this.description,
    required this.price,
    required this.rating,
    required this.type,
    required this.detailedDescription,
  });
}
